if [ -e .env ]; then
	. .env
elif [ -e ../.env ]; then
	pushd ..
	. .env
	popd
fi

# Install kubectl
curl -o kubectl https://amazon-eks.s3.us-west-2.amazonaws.com/1.19.6/2021-01-05/bin/linux/amd64/kubectl
chmod +x ./kubectl
mv ./kubectl /usr/local/bin
kubectl version --client
